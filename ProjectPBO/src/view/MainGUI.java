    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        int goodsId;
        try {
            goodsId = Integer.parseInt(id.getText());
        } catch (NumberFormatException e) {
            goodsId = 0;
        }
        if(goodsId == 0){
            DataRequired dataRequiredPanel = new DataRequired();
            dataRequiredPanel.setVisible(true);
        }else{
            String name = input_name.getText();
            double price = Double.parseDouble(input_price.getText());
            String stock = input_stock.getText();
            String selectedCategory = (String) input_category.getSelectedItem();
            Category category = Category.valueOf(selectedCategory);
            String brand = input_brand.getText();
            controller.updateGoods(goodsId, name, price, stock, category, brand);
            displayGoodsInTable();
        }
        clearForm();
    }//GEN-LAST:event_updateActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        int goodsId;
        try {
            goodsId = Integer.parseInt(id.getText());
        } catch (NumberFormatException e) {
            goodsId = 0;
        }
        if(goodsId == 0){
            DataRequired dataRequiredPanel = new DataRequired();
            dataRequiredPanel.setVisible(true);
        }else{
            controller.deleteGoods(goodsId);
            displayGoodsInTable();
        }
        clearForm();
    }//GEN-LAST:event_DeleteActionPerformed

