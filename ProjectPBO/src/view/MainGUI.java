    private void displayGoodsInTable() {
        List<Goods> goodsList = controller.getAllGoods();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Name");
        model.addColumn("Price");
        model.addColumn("Stock");
        model.addColumn("Category");
        model.addColumn("Brand");

        for (Goods goods : goodsList) {
            Object[] row = {
                goods.getId(),
                goods.getName(),
                goods.getPrice(),
                goods.getStock(),
                goods.getCategory(),
                goods.getBrand()
            };
            model.addRow(row);
        }

        goodsTable.setModel(model);
    }
    
    void filterGoodsInTable(String category){
        List<Goods> goodsList = controller.getAllGoods();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Name");
        model.addColumn("Price");
        model.addColumn("Stock");
        model.addColumn("Category");
        model.addColumn("Brand");
        String currentCategory;
        for (Goods goods : goodsList) {
            currentCategory = String.valueOf(goods.getCategory());
            if(currentCategory == category){
                Object[] row = {
                    goods.getId(),
                    goods.getName(),
                    goods.getPrice(),
                    goods.getStock(),
                    goods.getCategory(),
                    goods.getBrand()
                };
                model.addRow(row);
            }
        }
        goodsTable.setModel(model);
    }
    
