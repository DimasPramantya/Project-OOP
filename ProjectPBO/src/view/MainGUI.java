    private void actSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actSearchActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(inputId.getText());
        Goods goods = controller.findGoods(id);
        if(goods != null){
            this.id.setText(String.valueOf(goods.getId()));
            input_name.setText(goods.getName());
            input_price.setText(Double.toString(goods.getPrice()));
            input_stock.setText(goods.getStock());
            input_brand.setText(goods.getBrand());
            input_category.setSelectedItem(String.valueOf(goods.getCategory()));
        }else{
            DataNotFound notFoundPanel = new DataNotFound();
            notFoundPanel.setVisible(true);
        }
        inputId.setText("");
        searchForm.setVisible(false);
    }//GEN-LAST:event_actSearchActionPerformed

